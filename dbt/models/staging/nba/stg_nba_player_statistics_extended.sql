    percentTeamOffensiveRebounds            as percent_team_offensive_rebounds,
    percentTeamDefensiveRebounds            as percent_team_defensive_rebounds,
    percentTeamRebounds                     as percent_team_rebounds,
    percentTeamAssists                      as percent_team_assists,
    percentTeamTurnovers                    as percent_team_turnovers,
    percentTeamSteals                       as percent_team_steals,
    percentTeamBlocks                       as percent_team_blocks,
    percentTeamBlocksAgainst                as percent_team_blocks_against,
    percentTeamFoulsPersonal                as percent_team_fouls_personal,
    percentTeamFoulsDrawn                   as percent_team_fouls_drawn,
    percentTeamPoints                       as percent_team_points,
    ingested_at::timestamp                  as ingested_at
from source
